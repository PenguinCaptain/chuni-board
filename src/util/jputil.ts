export function containsJapanese(str: string): boolean {
    return /[\u3040-\u30ff\uff66-\uff9f\u3400-\u4dbf\u4e00-\u9faf]/.test(str);
}

export function convertKanjiToChinese(text: string): string {
    return text
        .split("")
        .map((char) => j2c[char] || char)
        .join("");
}

export function convertChineseToKanji(text: string): string {
    return text
        .split("")
        .map((char) => c2j[char][0] || char)
        .join("");
}

